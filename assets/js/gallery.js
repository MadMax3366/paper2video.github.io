function getRandomSubarray(arr, n) {
    var result = new Array(n),
        len = arr.length,
        taken = new Array(len);
    if (n > len)
        throw new RangeError("getRandom: more elements taken than available");
    while (n--) {
        var x = Math.floor(Math.random() * len);
        result[n] = arr[x in taken ? taken[x] : x];
        taken[x] = --len in taken ? taken[len] : len;
    }
    return result;
}

function renderGalleryTutorial() {
    var fiveRandomMembers = getRandomSubarray(id_tutorial, numItemsToGenerate);
    let gallery = document.getElementById("youtube_tutorial_list");
    gallery.innerHTML = '';
    for (let i = 0; i < numItemsToGenerate; i++) {
        let galleryItem = document.createElement('li');
        var row = fiveRandomMembers[i].split(/\r?\n/);
        galleryItem.innerHTML = `
            <div class="video-container">
                <iframe class="video" src="https://www.youtube.com/embed/${row[0]}"
                        allowfullscreen></iframe>
            </div>
            <p style="font-size: 90%; font-weight: lighter; text-align: left">
                    ${row[1]}</p>
        `
        gallery.appendChild(galleryItem);
    }
}


